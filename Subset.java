package Percolation;

import edu.princeton.cs.algs4.StdIn;
import edu.princeton.cs.algs4.StdOut;

public class Subset {

	public static void main(String[] args) {
		RandomizedQueue<String> itemList = new RandomizedQueue<>();
		
		 while (!StdIn.isEmpty()) {
	            String item = StdIn.readString();
	            itemList.enqueue(item);
	        }
	        for (int i = 0; i < i; i++) {
	            System.out.println(itemList.dequeue());
	      }
	 }
}
